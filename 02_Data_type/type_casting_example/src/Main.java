public class Main {
    public static void main(String[] args) {
        int i = 6;
        System.out.println("i = " + i);
        float j = i;
        System.out.println("j = "+j);

        float x = 6.5f;
        System.out.println("x = " + x);
        int y = (int) x;
        System.out.println("y = " + y);
    }
}